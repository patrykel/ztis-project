def get_stopwords(lang):
    """ Returns a stopwords list for a given language.

    @param lang Language code, e.g. "en"
    """
    if lang in LANG_FULL_NAME:
        return stopwords.words(LANG_FULL_NAME[lang])
    elif lang == "pl":
        return POLISH_STOPWORDS
    else:
        return []
